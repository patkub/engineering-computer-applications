// Print the poem The Road Not Take by Robert Frost.

#include <iostream>

using namespace std;

int main()
{
	// display the poem
	cout << "The Road Not Taken" << endl;
	cout << "By Robert Frost" << endl << endl;

	cout << "Two roads diverged in a yellow wood," << endl;
	cout << "And sorry I could not travel both" << endl;
	cout << "And be one traveler, long I stood" << endl;
	cout << "And looked down one as far as I could" << endl;
	cout << "To where it bent in the undergrowth;" << endl << endl;

	cout << "Then took the other, as just as fair," << endl;
	cout << "And having perhaps the better claim," << endl;
	cout << "Because it was grassy and wanted wear;" << endl;
	cout << "Though as for that the passing there" << endl;
	cout << "Had worn them really about the same," << endl << endl;

	cout << "And both that morning equally lay" << endl;
	cout << "In leaves no step had trodden black." << endl;
	cout << "Oh, I kept the first for another day!" << endl;
	cout << "Yet knowing how way leads on to way," << endl;
	cout << "I doubted if I should ever come back." << endl << endl;

	cout << "I shall be telling this with a sigh" << endl;
	cout << "Somewhere ages and ages hence:" << endl;
	cout << "Two roads diverged in a wood, and I-" << endl;
	cout << "I took the one less traveled by," << endl;
	cout << "And that has made all the difference." << endl << endl;

	system("pause");
	return 0;
}
